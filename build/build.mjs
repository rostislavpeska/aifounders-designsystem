/**
 * tokens.css + tokens-manifest.json emitter — the 3-layer color architecture
 * (operator, 2026-07-03) plus the non-color base tokens.
 *
 * LAYERS (colors):
 *   1 PALETTE   tokens/palette.{brand}.json — named unique values, the ONLY
 *               place color values live. Emitted per [data-theme].
 *   2 SEMANTIC  tokens/semantic.json — ONE vocabulary (~45 names), each a
 *               single-hop reference to a palette name. Emitted at :root and
 *               re-declared in full on light "reset" containers (popovers,
 *               .section-light) so they reset inside dark scopes.
 *   3 SCOPES    tokens/scopes/*.json — per-background DELTAS: the SAME
 *               semantic names re-declared with different palette refs.
 *               Reading a scope block IS the answer to "what changes there".
 *
 * LAWS: values only in the palette · semantic references palette, never a
 * sibling (single hop, kills the var() resolution gotcha) · scopes add no
 * new names. No Style Dictionary — this file is the whole build.
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const rd = (f) => JSON.parse(readFileSync(join(root, 'tokens', f), 'utf8'));

const base = rd('base.tokens.json');
const palAig = rd('palette.aiguild.json');
const palAif = rd('palette.aifounders.json');
const semantic = rd('semantic.json');
const divAig = rd('brand.aiguild.tokens.json');
const divAif = rd('brand.aifounders.tokens.json');
const SCOPES = [
  // [scope file, selector] — same harvested selector map as ever
  ['light-2', '.content-section--secondary'],
  ['light-3', '.content-section--tertiary'],
  // .footer REMOVED from dark-1 (operator 2026-07-06): live markup on BOTH
  // sites already emits `.footer.section-dark` — the alias was redundant
  // (persona precedent).
  ['dark-1', '.section-dark,\n.content-section--dark,\n.hero-card'],
  ['dark-2', '.content-section--dark-secondary,\n.dark-blurb--secondary,\n.footer__newsletter-section'],
  // .persona-card REMOVED from dark-3 (operator ruling 2026-07-04): one
  // canonical card, appearance from the BACKGROUND — the card is
  // surface-riding; consumers place it in a dark section for the dark look.
  ['dark-3', '.content-section--dark-tertiary'],
  ['brand', '.section-brand,\n.article-hero'],
  ['support', '.surface-support,\n.smart-btn'],
];

const refToVar = (v) => String(v).replace(/\{([a-z0-9-]+)\}/gi, 'var(--$1)');
const block = (selector, obj, indent = '  ') => {
  let out = `${selector} {\n`;
  for (const [k, v] of Object.entries(obj)) {
    if (k.startsWith('$')) continue;
    out += `${indent}--${k}: ${refToVar(v.$value)};${v.$description ? ` /* ${v.$description} */` : ''}\n`;
  }
  return out + '}\n';
};

let css = `/* GENERATED by build/build.mjs — DO NOT EDIT.\n * Source: tokens/*.json. Run: npm run tokens:build */\n\n`;

/* base (non-color: spacing, radius, shadows, containers, field scale…) */
css += '/* ── BASE (non-color) ' + '─'.repeat(56) + ' */\n';
css += block(':root', base) + '\n';

/* ── TYPOGRAPHY (operator layers, 2026-07-03) ──────────────────────────────
 * 1 sizes (pure, no line-height) · 2 leading + flow · 3 fonts + weights —
 *   primitives from tokens/typography.json
 * 5 style BUNDLES from tokens/type-styles.json — one var per property,
 *   single-hop refs to primitives; brand-diverged props (heading-md font…)
 *   live in the brand files and override via the [data-theme] blocks
 * 4 mobile — a style's mobile-size re-declares ITS OWN size var in one
 *   media block (the scope trick again: same name, different value)
 */
const typePrim = rd('typography.json');
const typeStyles = rd('type-styles.json');

/* ── BREAKPOINTS (operator-ratified 2026-07-03, docs/proposals/BREAKPOINTS.md)
 * Build-time constants, NOT custom props: CSS can't read var() inside @media,
 * so emitting --bp-* would be fiction. The build derives every query string
 * from these values; lint LAW 4 closes the set for hand-written CSS. */
const bpTokens = rd('breakpoints.json');
const bps = Object.entries(bpTokens)
  .filter(([k]) => !k.startsWith('$'))
  .map(([name, v]) => ({
    name,
    px: parseInt(v.$value, 10),
    bucket: v.$bucket || '',
    description: v.$description || '',
    evidence: v.$evidence || '',
  }))
  .sort((a, b) => a.px - b.px);
const bpPx = (name) => bps.find((b) => b.name === name).px;
css += '/* ── BREAKPOINTS (build-time constants — no --bp-* vars: CSS cannot read\n'
  + ' * custom props in @media) · BOUNDARY LAW: max side = value-1 ──\n * '
  + bps.map((b) => `${b.name} ${b.px}`).join(' · ')
  + ' · source: tokens/breakpoints.json · closed set: lint LAW 4 */\n\n';
css += '/* ── TYPE 1-3 · PRIMITIVES (sizes / leading+flow / fonts+weights) ' + '─'.repeat(12) + ' */\n';
css += block(':root', typePrim) + '\n';

let styleVars = '';
let mobileVars = '';
for (const [style, props] of Object.entries(typeStyles)) {
  if (style.startsWith('$')) continue;
  for (const [prop, v] of Object.entries(props)) {
    if (prop.startsWith('$')) continue; // $evidence = harvest record, not CSS
    if (prop === 'mobile-size') {
      mobileVars += `    --${style}-size: ${refToVar(v.$value)};\n`;
    } else {
      styleVars += `  --${style}-${prop}: ${refToVar(v.$value)};\n`;
    }
  }
}
css += '/* ── TYPE 5 · STYLE BUNDLES ' + '─'.repeat(50) + ' */\n';
css += ':root {\n' + styleVars + '}\n\n';

/* MECHANISM LAW (operator + Carbon, 2026-07-03): a style's responsive
 * behavior comes from its CLASS — display styles are FLUID (clamp), the
 * content ramp takes ONE step here, reading/UI styles are CONSTANT.
 * Mechanisms never diverge per brand; only values do. Emitted AFTER the
 * brand blocks (equal specificity — source order wins). */
const mobileBlock = '/* ── TYPE 4 · CONTENT-RAMP STEP (display=fluid clamp · reading/UI=constant) ── */\n'
  + `@media (max-width: ${bpPx('bp-md') - 1}px) {\n  :root {\n` + mobileVars + '  }\n}\n\n';

/* layer 1 — palette per brand (+ the few brand-diverged non-color tokens) */
css += '/* ── LAYER 1 · PALETTE (values live ONLY here) + brand-diverged type ' + '─'.repeat(8) + ' */\n';
css += block("[data-theme='aiguild']", { ...palAig, ...divAig }) + '\n';
css += block("[data-theme='aifounders']", { ...palAif, ...divAif }) + '\n';
css += mobileBlock;

/* layer 2 — one semantic vocabulary; also a full reset for light containers */
css += '/* ── LAYER 2 · SEMANTIC (the only names components use) ' + '─'.repeat(20) + ' */\n';
css += block(':root,\n.section-light,\n.form-select-menu,\n.datepicker-calendar', semantic) + '\n';

/* layer 3 — per-background deltas, same names */
css += '/* ── LAYER 3 · ON-BACKGROUND TRANSFORMS (deltas, same names) ' + '─'.repeat(16) + ' */\n';
for (const [scope, selector] of SCOPES) {
  const f = join(root, 'tokens', 'scopes', `${scope}.json`);
  if (!existsSync(f)) continue;
  const d = JSON.parse(readFileSync(f, 'utf8'));
  css += `/* ${scope} */\n` + block(selector, d) + '\n';
}

writeFileSync(join(root, 'assets/css/tokens.css'), css);
console.log('assets/css/tokens.css written:', css.length, 'bytes');

/* ── manifest for the styleguide (pages render FROM this) ─────────────────── */
const val = (pal, name) => (pal[name] ? String(pal[name].$value) : null);
const semRef = (v) => String(v.$value).replace(/[{}]/g, '');
const manifest = {
  palette: Object.fromEntries(Object.keys(palAig).filter((k) => !k.startsWith('$')).map((k) => [
    k, { aig: val(palAig, k), aif: val(palAif, k) },
  ])),
  semantic: Object.fromEntries(Object.entries(semantic).filter(([k]) => !k.startsWith('$')).map(([k, v]) => [
    k, { ref: semRef(v), aig: val(palAig, semRef(v)), aif: val(palAif, semRef(v)) },
  ])),
  scopes: Object.fromEntries(SCOPES.map(([scope]) => {
    const f = join(root, 'tokens', 'scopes', `${scope}.json`);
    if (!existsSync(f)) return [scope, {}];
    const d = JSON.parse(readFileSync(f, 'utf8'));
    return [scope, Object.fromEntries(Object.entries(d).filter(([k]) => !k.startsWith('$')).map(([k, v]) => [
      k, { ref: semRef(v), aig: val(palAig, semRef(v)), aif: val(palAif, semRef(v)) },
    ]))];
  })),
  base: Object.fromEntries(Object.entries(base).filter(([k]) => !k.startsWith('$')).map(([k, v]) => [
    k, { ref: String(v.$value), description: v.$description || '' },
  ])),
  typePrimitives: Object.fromEntries(Object.entries(typePrim).filter(([k]) => !k.startsWith('$')).map(([k, v]) => [
    k, { ref: String(v.$value), description: v.$description || '' },
  ])),
  typeStyles: Object.fromEntries(Object.entries(typeStyles).filter(([k]) => !k.startsWith('$')).map(([k, v]) => [
    k, Object.fromEntries(Object.entries(v).map(([p, pv]) =>
      p === '$evidence' ? ['evidence', String(pv)] : [p, String(pv.$value).replace(/[{}]/g, '')]
    )),
  ])),
  breakpoints: {
    cuts: Object.fromEntries(bps.map((b) => [b.name, {
      value: `${b.px}px`,
      px: b.px,
      bucket: b.bucket,
      min: `(min-width: ${b.px}px)`,
      max: `(max-width: ${b.px - 1}px)`, // BOUNDARY LAW: exclusive pairs
      description: b.description,
      evidence: b.evidence,
    }])),
    // buckets derive from the cuts: base + one per cut (to = next cut - 1)
    buckets: [
      { name: String(bpTokens['$base-bucket'] || 'base').split(' ')[0], from: 0, to: bps[0].px - 1, cut: null },
      ...bps.map((b, i) => ({
        name: b.bucket, from: b.px,
        to: i + 1 < bps.length ? bps[i + 1].px - 1 : null,
        cut: b.name,
      })),
    ],
  },
  brandType: Object.fromEntries(
    [...new Set([...Object.keys(divAig), ...Object.keys(divAif)])]
      .filter((k) => !k.startsWith('$'))
      .map((k) => [k, {
        aig: divAig[k] ? String(divAig[k].$value).replace(/[{}]/g, '') : null,
        aif: divAif[k] ? String(divAif[k].$value).replace(/[{}]/g, '') : null,
      }])
  ),
};
writeFileSync(join(root, 'assets/tokens-manifest.json'), JSON.stringify(manifest, null, 2) + '\n');
console.log('manifest:', Object.keys(manifest.palette).length, 'palette +',
  Object.keys(manifest.semantic).length, 'semantic tokens +',
  SCOPES.length, 'scopes');
